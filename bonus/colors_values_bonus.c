#include "so_long_bonus.h"

int	*colors_values(void)
{
	static int	colors[256];
	int			i;

	i = 0;
	while (i < 256)
	{
		colors[i] = i | (i << 8) | (i << 16);
		i++;
	}
	return (colors);
}
